package br.com.emotional.entidade;

/*
 * Classe responsável por criar instância de estilo musical
 */
public class EstiloMusical {
	private int id_estilo_musical; 
	private String nome_estilo_musical;
	public int getId_estilo_musical() {
		return id_estilo_musical;
	}
	public void setId_estilo_musical(int id_estilo_musical) {
		this.id_estilo_musical = id_estilo_musical;
	}
	public String getNome_estilo_musical() {
		return nome_estilo_musical;
	}
	public void setNome_estilo_musical(String nome_estilo_musical) {
		this.nome_estilo_musical = nome_estilo_musical;
	} 
	
	
}
